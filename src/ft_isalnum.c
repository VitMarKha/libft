#include "libft.h"

/*
** ft_isalnum: возвращает ненулевое значение,
** если его аргумент является либо буквой
** ал­фавита (верхнего или нижнего регистра),
** либо цифрой. Если символ не является
** буквенно-циф­ровым, возвращается 0.
*/

int	ft_isalnum(int c)
{
	return (((c >= 48) && (c <= 57)) ||
			((c >= 65) && (c <= 90)) ||
			((c >= 97) && (c <= 122))) ? 1 : 0;
}
